function getPaginaId() {
    const params = new URLSearchParams(window.location.search);
    return Number(params.get("id"));
}

function renderPagina() {

    const id = getPaginaId();
    const pagina = paginasData.find(p => p.id === id);
    if (!pagina) return;

    const pageHeader = document.querySelector(".page-header");
    const dica = document.querySelector(".dica-curiango");

    document.getElementById("card-title").innerText = pagina.titulo;

    // ===============================
    // 🔹 MODELO NOVO (secoes)
    // ===============================
    if (pagina.secoes && Array.isArray(pagina.secoes)) {

        if (pageHeader) pageHeader.style.display = "none";

        let html = "";

        // HERO
        if (pagina.hero) {
            const legendaHero = pagina.hero.legenda ? `<div class="hero-caption">${pagina.hero.legenda}</div>` : "";
            html += `
            <div class="hero-article" style="background:url('${pagina.hero.imagem}') center/cover;">
                <div class="hero-content">
                    <h1 style="font-size:30px; font-weight:800;">${pagina.titulo}</h1>
                    <p style="text-align: center; font-size:20px; padding: 0px 20px 0px 20px">${pagina.hero.subtitulo}</p>
                </div>
                ${legendaHero}
            </div>`;
        }

        const idsComTextoEmDuasColunas = [22, 23, 24, 25, 26];
        const classeDuasColunas = idsComTextoEmDuasColunas.includes(pagina.id) ? " texto-duas-colunas" : "";

        html += `<div class="article-container${classeDuasColunas}">`;

        pagina.secoes.forEach(sec => {
            const estiloSecao = [
                sec.fontSize ? `font-size:${sec.fontSize}` : "",
                sec.colunaUnica ? "column-count:1" : "",
                sec.colunaUnica ? "break-inside:avoid" : "",
                sec.textAlign ? `text-align:${sec.textAlign}` : ""
            ].filter(Boolean).join("; ");
            const attrEstiloSecao = estiloSecao ? ` style="${estiloSecao};"` : "";

            if (sec.tipo === "texto") {
                html += `<p${attrEstiloSecao}>${sec.conteudo ?? sec.texto ?? ""}</p>`;
            }

            if (sec.tipo === "titulo") {
                html += `<h2 class="section-title"${attrEstiloSecao}>${sec.texto ?? sec.conteudo ?? ""}</h2>`;
            }

            // imagem grande
            if (sec.tipo === "imagem") {
                html += `
        <div class="img-full">
            <img src="${sec.imagem}" class="img-premium">
            ${sec.legenda ? `<span class="img-caption">${sec.legenda}</span>` : ""}
        </div>`;
            }

            // duas imagens
            if (sec.tipo === "duas-imagens") {

                const height = sec.height ? sec.height : "450px";

                html += `<div class="row img-row">`;

                sec.imagens.forEach(img => {
                    const src = typeof img === "string" ? img : img.src;
                    const legenda = typeof img === "string" ? "" : img.legenda;
                    html += `
        <div class="col-md-6 col-12">
            <img src="${src}" class="img-premium" style="height:${height};">
            ${legenda ? `<span class="img-caption">${legenda}</span>` : ""}
        </div>`;
                });

                if (sec.legenda) {
                    html += `
        <div class="col-12">
            <span class="img-caption">${sec.legenda}</span>
        </div>`;
                }

                html += `</div>`;
            }

            // tres imagens
            if (sec.tipo === "tres-imagens") {

                const height = sec.height ? sec.height : "450px";

                html += `<div class="row img-row">`;

                sec.imagens.forEach(img => {
                    const src = typeof img === "string" ? img : img.src;
                    const legenda = typeof img === "string" ? "" : img.legenda;
                    html += `
        <div class="col-md-4 col-12">
            <img src="${src}" class="img-premium" style="height:${height};">
            ${legenda ? `<span class="img-caption">${legenda}</span>` : ""}
        </div>`;
                });

                if (sec.legenda) {
                    html += `
        <div class="col-12">
            <span class="img-caption">${sec.legenda}</span>
        </div>`;
                }

                html += `</div>`;
            }

            // quatro imagens
            if (sec.tipo === "quatro-imagens") {

                const height = sec.height ? sec.height : "450px";

                html += `<div class="row img-row">`;

                sec.imagens.forEach(img => {
                    const src = typeof img === "string" ? img : img.src;
                    const legenda = typeof img === "string" ? "" : img.legenda;
                    html += `
        <div class="col-md-6 col-12">
            <img src="${src}" class="img-premium" style="height:${height};">
            ${legenda ? `<span class="img-caption">${legenda}</span>` : ""}
        </div>`;
                });

                if (sec.legenda) {
                    html += `
        <div class="col-12">
            <span class="img-caption">${sec.legenda}</span>
        </div>`;
                }

                html += `</div>`;
            }

            // imagem + texto lado a lado
            if (sec.tipo === "imagem-texto") {
                const textoImagem = sec.texto ?? sec.conteudo ?? "";

                html += `
        <div class="row img-text-row">

            <div class="col-md-6 col-12">
                <img src="${sec.imagem}" class="img-premium">
            </div>

            <div class="col-md-6 col-12 texto-lateral">
                <p>${textoImagem}</p>
            </div>

        </div>`;
            }

            // box destaque
            if (sec.tipo === "box") {
                const classeBox = sec.imagemFundo ? " box-highlight--imagem" : "";
                const classeColunas = sec.colunas === 2 ? " box-highlight--duas-colunas" : "";
                const classeItens = sec.itens ? " box-highlight--itens" : "";
                const estiloBox = sec.imagemFundo ? ` style="background-image: linear-gradient(rgba(0,0,0,0.58), rgba(0,0,0,0.58)), url('${sec.imagemFundo}');"` : "";
                const conteudoBox = sec.itens
                    ? `<ul class="box-items">${sec.itens.map(item => `<li>${item}</li>`).join("")}</ul>`
                    : `<p>${sec.texto ?? sec.conteudo ?? ""}</p>`;
                const tituloBox = sec.titulo ? `<h3 class="section-title">${sec.titulo}</h3>` : "";

                html += `
        <div class="box-highlight${classeBox}${classeColunas}${classeItens}"${estiloBox}>
            ${tituloBox}
            ${conteudoBox}
        </div>`;
            }

        });

        html += `</div>`;

        document.getElementById("pagina").innerHTML = html;
    }

    // ===============================
    // 🔹 MODELO ANTIGO (pag)
    // ===============================
    else if (pagina.pag) {

        if (pageHeader) pageHeader.style.display = "block";
        document.getElementById("pagina").innerHTML = pagina.pag;
    }

    // ===============================
    // 🔥 CURIANGO PADRÃO COM LAYOUT FIXO
    // ===============================
    if (pagina.curiango) {

        if (dica) dica.style.display = "block";

        document.getElementById("curiango").innerHTML = `
        <div class="featured_category_item" data-aos="fade-up" data-aos-duration="800"
            style="background: #fff; border-radius: 20px; padding: 10px; box-shadow: 0 8px 25px rgba(0,0,0,0.08);">

            <div class="featured_category_info">

                <div style="display: flex; align-items: center; gap: 15px;">
                    <img src="images/WhatsApp Image 2024-02-14 at 21.04.28.jpg"
                        alt="Pássaro"
                        style="width: 100px; height: 80px; object-fit: cover; border-radius: 14px;">

                    <h4 class="featured_category_heading"
                        style="margin: 0; font-size: 20px; color: #f1833e; font-style: italic;">
                        Dica do Curiango
                    </h4>
                </div>

                <hr style="margin: 20px 0; border-color: #eee;">

                <div style="font-size: 16px; line-height: 23px; color: #555;">
                    ${pagina.curiango}
                </div>

            </div>
        </div>
    `;

    } else {

        if (dica) dica.style.display = "none";
        document.getElementById("curiango").innerHTML = "";

    }
}

if (!window.VTN_SKIP_RENDER_PAGINA) {
    document.addEventListener("DOMContentLoaded", renderPagina);
}
